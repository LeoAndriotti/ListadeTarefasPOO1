    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        btnVisualizar.doClick();
        int cod;
        cod= Integer.parseInt(JOptionPane.showInputDialog("QUAL O CÓDIGO DA TAREFA?"));
        if(JOptionPane.showConfirmDialog(null, "TEM CERTEZA QUE DESEJA REMOVER ESSA TAREFA? ","CONFIRME",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION)
        obj.remover(cod-1);
        txtListaGeral.setText(obj.visualizar());
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnExcluirTodasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirTodasActionPerformed
        if(JOptionPane.showConfirmDialog(null, "TEM CERTEZA QUE DESEJA EXCLUIR TODAS AS TAREFAS? ","CONFIRME",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION)
            obj.excluir();
        JOptionPane.showMessageDialog(null, "TAREFAS EXCLUIDAS COM SUCESSO");
        txtListaGeral.setText(obj.visualizar());
    }//GEN-LAST:event_btnExcluirTodasActionPerformed

