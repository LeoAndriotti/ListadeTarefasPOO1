    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        btnVisualizar.doClick();
        int cod;
        String novaTarefa;
        cod = Integer.parseInt(JOptionPane.showInputDialog("QUAL TAREFA SERÁ ALTERADA? "));
        novaTarefa = JOptionPane.showInputDialog("QUAL TAREFA SERÁ INSERIDA? ");
        obj.editar((cod-1), novaTarefa);
        txtListaGeral.setText(obj.visualizar());
    }//GEN-LAST:event_btnEditarActionPerformed

